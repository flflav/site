///////////////////////////////////////////////////////////////////////////////////////////
// GLOBAL VARIABLES
///////////////////////////////////////////////////////////////////////////////////////////

const Global = {
    currentPg: 0,
    destroyCss: false
}

///////////////////////////////////////////////////////////////////////////////////////////
// P5
///////////////////////////////////////////////////////////////////////////////////////////

const control = new p5((ctrl) => {
    ctrl.setup = () => {
        ctrl.noCanvas();
        ctrl.frameRate(30);
        ctrl.noLoop();
        ctrl.setHeader = new SetHeader();
        ctrl.setIcons = new SetIcons(ctrl);
        ctrl.setDestroy = new SetDestroy();
    }

    ctrl.draw = () => {
        if (Global.destroyCss) ctrl.setDestroy.destroy();
    }
});

///////////////////////////////////////////////////////////////////////////////////////////
// ONLOAD
///////////////////////////////////////////////////////////////////////////////////////////

window.onload = () => {
    setTimeout(() => {
        document.getElementById("cover").style.display = "none";
    }, 2000);
    setTimeout(() => {
        control.setHeader.setAnimation();
    }, 3000);
}